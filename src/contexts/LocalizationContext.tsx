interface LocalizationProviderProps {
  children: ReactNode;
}

export const LocalizationProvider: React.FC<LocalizationProviderProps> = ({ children }) => {
  const [language, setLanguageState] = useState<Language>('en');

  useEffect(() => {
    loadLanguage();
  }, []);

  const loadLanguage = async () => {
    try {
      const savedLanguage = await AsyncStorage.getItem('language');
      if (savedLanguage && Object.keys(translations).includes(savedLanguage)) {
        setLanguageState(savedLanguage as Language);
      }
    } catch (error) {
      console.error('Failed to load language:', error);
    }
  };

  const setLanguage = useCallback(async (newLanguage: Language) => {
    try {
      console.log(`Setting language from ${language} to ${newLanguage}`);
      setLanguageState(newLanguage);
      await AsyncStorage.setItem('language', newLanguage);
      console.log(`Language saved to AsyncStorage: ${newLanguage}`);
    } catch (error) {
      console.error('Failed to save language:', error);
    }
  }, [language]);

  const t = useCallback((key: string): string => {
    const result = translations[language][key] || key;
    console.log(`Translation: ${key} -> ${result} (language: ${language})`);
    return result;
  }, [language]);

  const value: LocalizationContextType = useMemo(() => ({
    language,
    setLanguage,
    t,
  }), [language, setLanguage, t]);

  return (
    <LocalizationContext.Provider value={value}>
      {children}
    </LocalizationContext.Provider>
  );
};

export const useLocalization = (): LocalizationContextType => {
  const context = useContext(LocalizationContext);
  if (context === undefined) {
    throw new Error('useLocalization must be used within a LocalizationProvider');
  }
  return context;
};